        // Setting up default background color
        defaultcolor = firstnameField.getBackground();
        
        // Set firstname
        if (firstnameField.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Please enter you firstname");
        } else {
            if (p.getFirstName() == null){
            p.setFirstName(firstnameField.getText());
            count++;
        }}
        
        // Set lastname
        if (lastnameField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you lastname");
        } else {
            if (p.getLastName() == null){
                p.setLastName(lastnameField.getText());
                count++;
        }}
        
        // Set middlename
        if (middlenameField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you middlename");
        } else {
              if (p.getMiddleName()== null){
                p.setMiddleName(middlenameField.getText());
                count++;
        }}
        
        // Set Area code
        if (areacodeField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you areacode");
        } else {
              if (p.getAreaCode()== null){
                p.setAreaCode(areacodeField.getText());
                count++;
        }}

        // Set Zip Code
        if (zipcodeField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you zipcode");
        } else {
              if (p.getZipcode()== null){
                p.setZipcode(zipcodeField.getText());
                count++;
        }}
        
        // Set Town name
        if (townField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you townfield");
        } else {
              if (p.getTown()== null){
                p.setTown(townField.getText());
                count++;
        }}
        
        // Set Street name
         if (streetField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter you street name");
        } else {
               if (p.getStreetAddress()== null){
                p.setStreetAddress(streetField.getText());
                count++;
        }}
        
         // Set Phone number
          if (phonenumberField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter your phone number ");
        } else {
                if (p.getPhoneNumber()== null){
                    if (phonenumberField.getText().length()==10){
                    p.setPhoneNumber(phonenumberField.getText());
                    count++;
                    } else {
                    JOptionPane.showMessageDialog(this, "Please enter 10 digit phone number ");
                }
        }}

        // Set occupation field
        if (occupationField.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please let us know about your occupation");
        } else {
              if (p.getOccupation() == null)
              {
                p.setOccupation(occupationField.getText());
                count++;
              }
        }
        
        // Confirming date, month and year to be under 31, 12 and 2015 respectively
        if ((dobField.getText().length())!=0) {
            String[] dob = dobField.getText().split("/");
            int month = Integer.parseInt(dob[0]);
            int date = Integer.parseInt(dob[1]);
            int year = Integer.parseInt(dob[2]);
            if (p.getDateOfBirth()== null){
                if ((date <= 31) && (month <=12) && (year <= 2015)) {
                    p.setDateOfBirth(dobField.getText());
                    count++;
                } else { 
                    JOptionPane.showMessageDialog(this,"Error.Please insert date in correct format");  }
            } else { }
        }else
            {
                JOptionPane.showMessageDialog(this,"Please enter your date of birth");
        }
        
        // If count is 10, confirms that all the fields have value. Save the date
        if(count==10)
            JOptionPane.showMessageDialog(this,"Data saved.");
        else
            JOptionPane.showMessageDialog(this, "Please fill all the fields to proceed..");
         
           
