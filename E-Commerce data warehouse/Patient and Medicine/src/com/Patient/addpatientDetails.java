    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed
        
        int selectedrow = personlistjTable.getSelectedRow();
        if(selectedrow >=0)
        {
            Person peeps = (Person) personlistjTable.getValueAt(selectedrow,0);
            if(track(peeps)){
            Patient patient = peeps.getvitalsignhistory().addPatient();
            String patient1 = patient.getPatientId();
            if((patient1 == null)){
                    patient.setPatientId(patientidjTextField.getText());
                    patient.setDoctorName(doctornamejTextField.getText());
                    patient.setPharmacy(pharmacynamejTextField.getText());
                JOptionPane.showMessageDialog(this, "Patient is added.","Information", JOptionPane.INFORMATION_MESSAGE);
                    
            } else {
                    JOptionPane.showMessageDialog(this, "Please select a row","Warning", JOptionPane.ERROR_MESSAGE);
        } }
            else{
                JOptionPane.showMessageDialog(this, "Patient details are already set.","Warning", JOptionPane.ERROR_MESSAGE);
                }
     } else {
            JOptionPane.showMessageDialog(this, "Please select a row","Warning", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addjButtonActionPerformed

    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        
        int selectedrow = personlistjTable.getSelectedRow();
        if(selectedrow >=0)
        {
            Person person = (Person) personlistjTable.getValueAt(selectedrow,0);
            
            UpdatePerson va = new UpdatePerson(userprocesscontrol,person,this);
            userprocesscontrol.add("UpdatePerson",va);
            CardLayout layout = (CardLayout) userprocesscontrol.getLayout();
            layout.next(userprocesscontrol);
        }  
        else {
            JOptionPane.showMessageDialog(this, "Please select a row","Warning", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_updatejButtonActionPerformed

