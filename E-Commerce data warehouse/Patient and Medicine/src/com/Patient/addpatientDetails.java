    public void populatetable(){
    
     DefaultTableModel dtm = (DefaultTableModel)personlistjTable.getModel();
        int rowcount = personlistjTable.getRowCount();
                
                
        for ( int i= rowcount -1 ; i>=0; i--)
            dtm.removeRow(i);
        
        for (Person per : persondirectory.getpersonlist())
        {
            Object row[] = new Object[3];
            row[0] = per;
            row[1] = per.getAge();
            row[2] = per.getMailid();
            dtm.addRow(row);
        }
    }
